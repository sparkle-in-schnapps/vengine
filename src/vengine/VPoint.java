package vengine;

/**
 *
 * @author yew_mentzaki
 */
public class VPoint {
    public double x, y, z;

    public VPoint(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public VPoint(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public VPoint(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
}
